/* ============================================================
   Langue active — détection + persistance
   ============================================================ */

const CLE_STORAGE = 'kala_langue';
const LANGUE_DEFAUT = 'fr';

function detecterLangue() {
  const sauvegardee = localStorage.getItem(CLE_STORAGE);
  if (sauvegardee && TRADUCTIONS[sauvegardee]) return sauvegardee;

  const navigateur = (navigator.language || navigator.userLanguage || '').split('-')[0].toLowerCase();
  if (TRADUCTIONS[navigateur]) return navigateur;

  return LANGUE_DEFAUT;
}

let _langueCourante = detecterLangue();

/* ============================================================
   API publique
   ============================================================ */

export const i18n = {

  /* Langue active */
  get langue() { return _langueCourante; },

  /* Changer de langue */
  setLangue(code) {
    if (!TRADUCTIONS[code]) return;
    _langueCourante = code;
    localStorage.setItem(CLE_STORAGE, code);

    /* RTL */
    const info = LANGUES.find(l => l.code === code);
    document.documentElement.dir  = info?.rtl ? 'rtl' : 'ltr';
    document.documentElement.lang = code;

    /* Mettre à jour tous les éléments [data-i18n] dans la page */
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const cle = el.getAttribute('data-i18n');
      el.textContent = i18n.t(cle);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const cle = el.getAttribute('data-i18n-placeholder');
      el.placeholder = i18n.t(cle);
    });
  },

  /* Traduire une clé */
  t(cle, fallback = '') {
    const trad = TRADUCTIONS[_langueCourante];
    if (trad && trad[cle] !== undefined) return trad[cle];
    const fr = TRADUCTIONS[LANGUE_DEFAUT];
    if (fr && fr[cle] !== undefined) return fr[cle];
    return fallback || cle;
  },

  /* Appliquer i18n à toute la page courante */
  appliquer() {
    const info = LANGUES.find(l => l.code === _langueCourante);
    document.documentElement.dir  = info?.rtl ? 'rtl' : 'ltr';
    document.documentElement.lang = _langueCourante;

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const cle = el.getAttribute('data-i18n');
      el.textContent = i18n.t(cle);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const cle = el.getAttribute('data-i18n-placeholder');
      el.placeholder = i18n.t(cle);
    });
  },

  /* Rendu du sélecteur de langue (HTML) */
  renderSelecteur(opts = {}) {
    const { classes = '', onchange = '' } = opts;
    const options = LANGUES.map(l => {
      const selected = l.code === _langueCourante ? 'selected' : '';
      return `<option value="${l.code}" ${selected}>${l.drapeau} ${l.nom}</option>`;
    }).join('');

    return `
      <select class="lang-select ${classes}" aria-label="${i18n.t('lang.choisir')}" ${onchange ? `onchange="${onchange}"` : ''}>
        ${options}
      </select>
    `;
  },

  /* Liste des langues pour construire un panneau custom */
  getLangues() {
    return LANGUES;
  },

};

/* Alias court */
export const t = (cle, fallback) => i18n.t(cle, fallback);

export default i18n;
